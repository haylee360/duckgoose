#' Cool math function
#'
#' A function that squares x and squares y then adds them
#'
#' @param x a numeric value of x
#' @param y a numeric value of y
#'
#' @return a numeric value that is the sum of the squares of x and y
#' @export
#'
#' @examples cool_math(4, 5)
cool_math <- function(x, y) {
  x^2 + y^2
}
